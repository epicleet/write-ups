#!/usr/bin/python3

def convert(code):
    if code & 0xf0 == 0x80:
        return code ^ 0xe8
    if code & 0xf0 == 0x90:
        return code ^ 0xf8
    if code & 0xf0 == 0xa0:
        return code ^  0xd8
    return code ^ 0x28

def sub(code):
    return code - 0x28

a = [0x8e, 0x94, 0x89, 0x8f, 0xa3, 0x9d, 0x87, 0x90, 0x5c, 0x9e, 0x5b, 0x87, 0x9a, 0x5b, 0x8b, 0x58, 0x9e, 0x5b, 0x9a, 0x5b, 0x8c, 0x87, 0x95, 0x5b, 0xa5]
b = [convert(x) for x in a]
c = [sub(x) for x in a]

print(a)
print(b)
print(c)
print(bytes(b))
print(bytes(c))

